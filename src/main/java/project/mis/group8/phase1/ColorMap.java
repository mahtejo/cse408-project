package project.mis.group8.phase1;

public class ColorMap {
	int x_bits = 0, y_bits = 0, z_bits = 0;
	int x_buckets = 0, y_buckets = 0, z_buckets = 0;

	public ColorMap() {

	}

	public int getX_bits() {
		return x_bits;
	}

	public void setX_bits(int x_bits) {
		this.x_bits = x_bits;
	}

	public int getY_bits() {
		return y_bits;
	}

	public void setY_bits(int y_bits) {
		this.y_bits = y_bits;
	}

	public int getZ_bits() {
		return z_bits;
	}

	public void setZ_bits(int z_bits) {
		this.z_bits = z_bits;
	}

	public int getX_buckets() {
		return x_buckets;
	}

	public void setX_buckets(int x_buckets) {
		this.x_buckets = x_buckets;
	}

	public int getY_buckets() {
		return y_buckets;
	}

	public void setY_buckets(int y_buckets) {
		this.y_buckets = y_buckets;
	}

	public int getZ_buckets() {
		return z_buckets;
	}

	public void setZ_buckets(int z_buckets) {
		this.z_buckets = z_buckets;
	}
}
